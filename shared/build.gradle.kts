buildkonfig {
    packageName = "com.example.firstproject"
    defaultConfigs {
        // 1. Load local.properties
        val localProperties = Properties()
        val localPropertiesFile = rootProject.file("local.properties")
        if (localPropertiesFile.exists()) {
            localPropertiesFile.inputStream().use { localProperties.load(it) }
        }

        // 2. Get the key, or use dummy if not found
        val apiKey = localProperties.getProperty("NEWS_API_KEY") ?: "dummy_api_key"

        // 3. Inject it into the Kotlin code
        buildConfigField(com.codingfeline.buildkonfig.compiler.FieldSpec.Type.STRING, "NEWS_API_KEY", apiKey)
    }
